                <h1> Web designer, qu’est ce que c’est ? </h1>
                <p> Article réalisé et rédigé par <a href="https://github.com/Onetouch59" target="_blank"><span class="lightgrey">Adam B</span> .</a></p>
            </div>

            <div class="article__group">
                <h2 class="purple"> Son rôle </h2>

                <p>    
                    C’est de concevoir le graphisme d’un site ou d’un produit sur une page web. Le webdesigner appelé également infographiste ou graphiste est, selon la formule consacrée, « <span class="bold">le créatif multimédia du numérique</span> ». C’est lui qui va traduire en style graphique le brief du client. <br><br>

                    Directeur artistique version Web, le webdesigner va concevoir et réaliser le design du site Web. Le webdesigner voit son périmètre d’intervention différer suivant la structure où il intervient. Dans une PME ou en indépendant, sa liberté sera d’autant plus grande qu’il condensera entre ses mains l’ensemble de la chaîne de conception graphique et artistique.
                </p> 

                <img src="../assets/images/pictures/adam_picture_one.jpg" alt="Conception d'une interface graphique">

                <p> 
                    Dans les entreprises plus spécialisées, le webdesigner travaillera en équipe, en collaboration étroite avec un chef de projet ou des responsables artistiques et éditoriaux qui guideront et valideront son travail au fur et à mesure de l’avancement du projet. En liaison étroite avec le chef de projet et le webmaster, le webdesigner participe à la définition du cahier des charges du site à créer ou à faire évoluer : identité visuelle, charte graphique, maquette, choix des technologies utilisées pour la partie front du site. <br><br>

                    Ce travail a un seul et unique but : capter l’attention des visiteurs et leur permettre de naviguer dans le site dans les meilleures conditions de compréhension et de vitesse. Directeur artistique, le webdesigner choisit le style et la place des visuels, le type et les tailles des typographies, agence et mélange les couleurs, conçoit les éventuelles animations pour permettre la meilleure hiérarchisation des éléments possibles.
                </p> 
            </div> 

            <div class="article__group">
                <h2 class="purple"> Prérequis </h2>

                <p>
                    Avoir une bonne sensibilité visuelle, s’intéresser aux tendances graphiques du moment, ce qui suppose aussi une ouverture culturelle tous azimuts. <br><br>

                    Le webdesigner doit tenir compte des demandes de ses clients. Il doit donc être à l’écoute, s’imprégner de leurs codes, intégrer leurs contraintes ( <span class="mediumgrey">cahier des charges</span> ). Il faut donc avoir des qualités relationnelles, être curieux, patient, et savoir faire des propositions adaptées. Pas question de s’enfermer dans sa tour d’ivoire comme un artiste génial et incompris !
                </p>

                <img src="../assets/images/pictures/adam_picture_two.jpg" alt="Personne à l'écoute d'une autre">
            </div>

            <div class="article__group">
                <h2 class="purple"> Evolution </h2>

                <p>
                    Le webdesigner est proche de l’ergonome, spécialiste de l’expérience utilisateur ( <span class="mediumgrey">UX / UI Design</span> ) <br><br>

                    Il travaille aussi de près avec l’intégrateur web qui va coder, c’est-à-dire réaliser techniquement un site web ou une appli avec le langage informatique adapté pour mettre en ligne le visuel imaginé par le webdesigner. Le graphiste web qui maîtrise lui-même ces langages aura donc une corde de plus à son arc pour se mettre à son compte ou créer sa propre agence. <br><br>
                    
                    Enfin, il pourra bien sûr évoluer vers le marketing digital et les métiers de la communication, mais aussi la création d’image 3D, de jeux vidéo ou d’applications mobiles. A moins qu’il soit à l’aise dans la coordination, le management, et évolue vers des postes de chef de projet. <br><br>
                </p>
