            <div class="article__group">
                <p>
                Quand on parle des technologies numériques, on y pense généralement le milieu de l’internet.
                Le monde du web ne cesse de s’accroître beaucoup à nos jours et permet à chaque pays d’accélérer leur développement socio-économique. 
                </p>

                <img src="../assets/images/pictures/tiavina_picture_one.jpg" alt="couv">

                <p>La concurrence se pose alors entre les grandes agences web, les start-ups ainsi que les freelances.</p>
            </div>

            <div class="article__group">
                <h2 class="purple">C’est quoi une agence web ?</h2>

                <p>Une agence web commence un projet par la conception d’une maquette graphique mettant en valeur l’identité visuelle du client . Ensuite , il acheminera le projet par la rédaction de son contenu et le finalise par son intégration. <br><br>

                Les taches des activités que tient une agence web est de produire pour son client et réaliser un projet donc logiquement élargie au-delà de la création de site internet.
                Les agences web détiennent des rôles importants sur la gestion des interfaces de pages, des sites, des publications personnels comme les blogs ou même les pages personnelles comme les portfolios, curriculum vitae numérique et biens d’autres.
                Sans oublier que c’est un enseigne très riche et développé dans le temps , il ne cesse aussi de s’évoluer en matière de jeux vidéo , plateformes de forums , réseaux sociaux et touts applications mobiles.
                Une agence web est donc une entreprise spécialisée dans la création et la mise à jour de sites web et tout ce qui est interfaces web comme on vient de citer en haut. <br><br>

                L’agence web accompagne ses clients dans l’ensemble des actions mises en œuvre pour communiquer sur Internet. Elle est capable d’avoir une vision d’ensemble d’un projet web et d’agir sur plusieurs rôles, notamment par le biais de la stratégie digitale. Mais aussi, une agence web maîtrise les aspects technique, esthétiques, commerciaux nécessaires à la réussite des projets de communication. <br><br>

                Dans les années 2000, les agences web ont vu leur domaine d’intervention s’élargir à mesure que le web prenait de l’ampleur et que la concurrence sur la toile s’intensifiait.
                </p>
            </div>

            <div class="article__group">
                <h2 class="purple">Quels services proposent-ils ?</h2>

                <p>
                    Une agence web, vous aidera à valider la pertinence et la faisabilité d’une idée. Tout en réalisant une étude de marché précise, et une évaluation des contraintes techniques que vous rencontrerez. <br><br>

                    Dans le prolongement du projet de site web, vous pourrez établir le cahier des charges avec elle. Et après ces étapes de préparation, vient l’étape de conception et de réalisation du site web jusqu’à sa mise en ligne. Une coopération étroite sera la bienvenue entre le client et le prestataire, afin de construire une communication vraiment qualitative. <br><br>

                    Enfin, l’agence web vous accompagnera dans la durée, pour l’hébergement de votre site web, la résolution des problèmes techniques, de sécurité et de mises à jour. <br><br>

                    Elle vous aidera également dans la diffusion de vos messages, en utilisant le référencement naturel ( <span class="mediumgrey">SEO</span> ) ou le référencement payant ( <span class="mediumgrey">SEA</span> ), les réseaux sociaux, etc. <br><br>

                    Ils peuvent aussi proposés aux clients, d’autres solutions comme les CMS : Joomla, Wix ou WordPress, qui possède un meilleur rapport qualité / prix que d’autres.
                </p>
            </div>

            <div class="article__group">
                <h2 class="purple">Qu’attendent-les clients ?</h2>

                <p>Une agence web fait en sorte que son client soit assuré et satisfait . Afin de lui proposer une service ou solution qui lui convient jusqu’à la mise en ligne de son projet.</p>

                <img src="../assets/images/pictures/tiavina_picture_two.jpg" alt="couv">

                <h2>Conclusion</h2>

                <p>Les agences web assistent et conseillent leurs clients dans leurs projets de communication digitale. Ce sont des spécialistes qui cumulent des compétences en marketing pour assurer la promotion des sites web et des compétences techniques ( <span class="mediumgrey">développement web, Webdesign</span> ). <br><br>
                
                Vous pourrez trouver auprès d’elles des réponses pour des sujets aussi divers que l’identité de votre marque, l’hébergement de votre site web, l’acquisition de trafic sur le web, l’utilisation des mails, etc.
                Dans tous les cas, une agence web doit vous donner une visibilité unique et personnelle ainsi qu’une image moderne et esthétique.
                </p>
            </div>

