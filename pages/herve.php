                <h1> Le métier de Community Manager </h1>
                <p> Article réalisé et rédigé par <a href="https://github.com/Micmacthebigmac" target="_blank"><span class="lightgrey">Hervé R</span> .</a></p>
            </div>

            <div class="article__group">
                <p>
                    Le community manager est une personne qui possède plusieurs casquette. La principale étant d’être le médiateur entre une communauté et la structure pour laquelle il travaille. Il est donc le porte parole de l’entreprise ou de la marque sur internet.
                </p>

                <img src="../assets/images/pictures/herve_picture_one.jpg" alt="Community manager qui prends la parole devant ses collègues">

                <p>
                    Son travail consiste en amont à créer et à gérer une communauté.
                    Il a besoin d’une connaissance solide de la structure, une simple information peut être primordiale pour répondre à sa communauté.
                </p>
            </div>  

            <div class="article__group">
                <h2 class="purple"> En quoi consiste son travail ? </h2>

                <p>
                    Pour atteindre son objectif, un community manager se voit confier plusieurs responsabilités : <br><br>

                    
                    La modération des comptes sur les plateformes et réseaux sociaux : répondre aux questions via les blogs, forums et réseaux sociaux. <br>
                    L’organisation des jeux et concours online et des rencontres irl ( <span class="mediumgrey">in real life</span>  ). <br>
                    La stratégie de community management : établir la stratégie sur le recrutement, l’animation et la fidélisation de la communauté en utilisant des outils pertinents.
                </p>

                <img src="../assets/images/pictures/herve_picture_two.jpg" alt="reunion de travail">

                <p>
                    La rédaction du contenu pour chaque plateforme, réseau social choisi. <br> 
                    La veille concurrentielle. <br>
                    L’e-reputation de la marque. <br>
                    Le reporting des actions menées. <br>
                    L’administratif, la comptabilité, le commercial ( <span class="mediumgrey">prospection et suivi</span> ) pour le community manager freelance.
                </p>
            </div> 

            <div class="article__group">   
                <h2 class="purple"> Devenir community manager </h2>

                <p>
                Devenir community manager à l’aube des années 2010 était un parcours du combattant. Le peu de formations étant concentrées sur Paris, de nombreux profils passionnés par le rôle ont appris seul. Le métiers est donc souvent appris en auto-formations.
                </p>
