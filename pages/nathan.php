            <h2 class="red"> Ai-je les qualités et compétences nécessaires ? </h2>

            <p>
                Il doit prendre en compte les compétences de chacun et leur donner des missions cohérentes avec cela. À l’écoute, il possède de grandes capacités d’organisation. Il doit également être rigoureux. C’est même le garant de la rigueur en termes de temps, de budget, de ressources… du projet.
            </p>

            <img src="../assets/images/pictures/nathan_couv.jpg" alt="couv">

            <p>
                Très intéressé par le domaine des technologies, il a des compétences techniques en informatique et il sait de quoi il parle avec ses collaborateurs. Ce sont les formations informatiques, ingénieurs, ou encore jeux vidéos qui donnent ces compétences au chef de projet numérique.
            </p>

            <p>
                <span class="bold"> Niveau rémunération ? </span> <br><br>

                Environ 1 850 € brut par mois pour un chef de projet numérique débutant, avec une progression qui peut être rapide selon la réussite des projets. <br>
                Les salaires des chefs de projet numérique varient selon leur expérience et la taille de l’entreprise.
            </p>
