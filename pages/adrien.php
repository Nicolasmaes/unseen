                <h1> La cryptographie ou l'art du secret </h1>
                <p> Article réalisé et rédigé par <a href="https://github.com/Adrien-Ray" target="_blank"> <span class="lightgrey">Adrien R</span></a> .</p>
            </div>

            <div class="article__group">
                <p>
                    La cryptographie regroupe les disciplines ayant pour but de rechercher des moyens d’assurer la protection de données. <br> 
                    Cela inclu : <br><br><br>
                    
                    <span class="bold">la confidentialitée</span> <br>
                    Empêcher la lecture d’un message par une personne non-autorisée. <br><br>

                    <span class="bold">l’authenticitée</span> <br> 
                    Permettre de « signer » un message pour assurer qu’il est envoyer par la bonne personne. <br><br>

                    <span class="bold">l’intégritée</span> <br>
                    Mise en place de protocoles permettant de vérifier que le message n’a pas été modifier de manière frauduleuse. <br>
                    
                </p>

                <img src="../assets/images/pictures/adrien_picture_one.jpg" alt="">

                <p>
                    Lexique : <br><br>
                    
                    <span class="bold">Chiffrement</span> <br>
                    Remplacer les caractère d’un texte « en claire » par d’autre pour le rendre illisible. <br><br>

                    <span class="bold">Cryptosystème</span> <br>
                    Algorithme de chiffrement. <br><br>

                    <span class="bold">Cryptanalyse</span> <br>
                    Science de l’étude des messages chiffrées en vue de leur déchiffrement. <br><br>

                    <span class="bold">Cryptologie</span> <br>
                    Science regroupant la cryptographie et la cryptanalyse. <br>
                </p>
        </div>  

        <div class="article__group">
            <h2 class="red"> La cryptographie dans le numérique </h2>

            <p>
                Dans le numérique, la cryptographie est omni-présente. D’abord utilisé dans les communications militaires, puis dans les messages relevants du secret d’état en général ( <span class="mediumgrey">sphère diplomatique</span> ). Elle s’est continuellement perfectionné et démocratisé. En effet, les secteurs bancaires et industriels ont rapidement compris que la posession de technologie fiables serait vital, pour sécurisé les informations bancaires et contrer l’espionnage industriel. Aujourd’hui, sur internet, le simple fait de rentrer sur un espace personel demande par exemple : <br><br>

                un procédé cryptographique de certification du dommaine ( <span class="mediumgrey">pour s’assurer que l’on ne se trouve pas sur un faux site destiné à voler notre mot de passe via le formulaire d’entrée</span> ). <br><br>

                un procédé cryptographique pour rendre identifiants et mots de passe illisible à un attaquant qui interceperait la communication. <br><br>

                un procédé cryptographique pour rendre illisible les pages qui s’échangeront après authentification, puisque celles-ci pourront avoir pour contenu ce qui est censé être protégé par le mot de passe.
            </p>

            <img src="../assets/images/pictures/adrien_picture_two.jpg" alt="">
        </div> 

        <div class="article__group">  
            <h2 class="red"> La cryptographie dans les métiers du numérique </h2>

            <p>
                
                <span class="bold">Developpeur cryptgraphe</span> <br><br>

                Developpeur spécialiser dans le cryptage des données sensibles. <br><br><br>

                
                <span class="bold">Analyste en sécurité</span> <br><br>

                Vérifie que les systemes de sécuritée d’une entreprise/d’un réseau fonctionnes et répondent aux standards actuels. <br><br><br>

                
                <span class="bold">Ingénieur en sécurité</span> <br><br>

                Métier consistant a élaborer la sécuritée d’un réseau ( <span class="mediumgrey">proche de analyste en sécuritée, qui prend en quelque sorte la suite des opérations</span> ).<br><br><br>

                
                <span class="bold">Consultant en sécurité</span> <br><br>

                Il s’agit fréquement d’une personne/organisme exterieur qui donne des conseils, des recommendations et aide a la mise en place de ceux-ci. <br><br><br>

                
                <span class="bold">Pentester</span> <br><br>

                Le pentesteur cherche des failles de sécuritée dans le but de les corrigé. en claire, il s’agit de faire le travail d’un pirate pour réglé les failles avant qu’un réel pirate ne les exploites. Le pentester est souvent confronté a la cryptographie, il peut être notament utile qu’il soit formé à la cryptanalyse. ( <span class="mediumgrey">voir Le Pentesteur ou le Corsaire des Hackers, sur la fiche wiliams.md</span> )<br><br><br>

                
                <span class="bold">Technicien réseau</span> <br><br>
                Le technicien réseau peut être amené à géré la sécuritée des serveurs et des données échangées, il peut donc aussi lui être demandé d’avoir des compétences en cryptologie. <br><br><br>

                
                <span class="bold">Cryptologue</span> <br><br>

                Cryptologue n’est pas un métier que l’on est ammené a trouver fréquement dans une entreprise. Il s’agit en fait plus d’un mathématicien ( <span class="mediumgrey">voir d’un physicien</span> ) qui invente de nouvelles méthodes de cryptage ( <span class="mediumgrey">voir qui cherche les méthodes permettant de casser le code sans la clef</span> ). Il existes de nombreuses sous-disciplines dans ce dommaine ( <span class="mediumgrey">Cryptanalyse, Cryptographie symétrique, Cryptographie asymétrique, Cryptographie quantique et post-quantique</span> ). <br><br>
            </p>
        </div>

        <div class="article__group">
            <h2 class="red"> Conclusion </h2>

            <p>
                La cryptologie regroupe un grand nombre de compétences. De même, il existe une grande variété de métiers dans lesquels il peut être nécessaire d’avoir des notions de cryptographie. A partir du moment ou un système, un réseau, une entreprise manipule des données dont la lecture sans autorisation peut être convoitée, il est nécessaire qu’une personne compétente dans ce domaine réfléchisse à la mise en place et la maintenance des systèmes cryptographiques.
            </p>
