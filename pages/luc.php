            <div class="article__group" >
                <h2 class="purple">Fonctions</h2>

                <p>
                    Bien qu'il intervienne sur tout les fronts, on peut réduire son poste a trois fonctions cruciale. <br><br>
                    
                    La conception d'application ( <a href=""><span class="mediumgrey"> Développeur web </span></a> ) <br>
                    L'admnistration et le bon fonctionnement de l'application ( <a href=""><span class="mediumgrey">Administrateur système</span></a> ) <br>
                    Un controle qualité sur chaque étapes ( <a href=""><span class="mediumgrey"> Devops </span> )</a>
                </p>

                <img src="../assets/images/pictures/luc_picture_two.jpg" alt="Reflexions autour d'un projet">

                <p>
                    Ces connaissances sont extrêmement vastes :<br><br>
                    
                    Ecriture et connaissance du code binaire, mise au point de scripts, intégration, maitrise des différents langages de prog ( <span class="mediumgrey">Javascript, C+, html, css, php, .net</span> ) <br>
                    Installation d'infrastructures ( <span class="mediumgrey">création de passerelles de transferts de données</span> ) <br>
                    Controle qualité ( <span class="mediumgrey">évaluation des applications, mesures de performances, ajustements correctifs</span> )
                </p>
            </div>

            <div class="article__group">
                <h2 class="purple"> Parcours, débouché et rémunérations </h2>

                <p> 
                    Il n'y a jusqu'a présent aucun parcours type pour accéder a la profession. La majorité des devops sont d'anciens ingenieurs informatique, curieux et touche à tout. ils se sont     orienté au gré du temps et de l'expérience vers cette discipline. <br><br>

                    En ce qui concerne le recrutement, le rapport de force joue en faveur du devops. Il y a peu de professionnels pour beaucoup d'entreprises en demande. <br>
                    En début de carrière, le salaire journalier est aux alentours des 600 euros.
                </p>
