        <h1>Mission de l’animateur graphique (motion designer web)</h1>
        <p>par <a href="https://github.com/etiennezastko" target="_blank">Etienne Z.</a></p>
        <img src="../assets/images/pictures/etienne-1.png" alt="couv">
        <h2>Le rôle la perspective du motion designer</h2>
        <ul>
        <li>L’animation graphique est le métier du motion designer qui scénarise et met en scène l’information dans une logique animée voir narrative. Les notions de mouvements, de déplacements, de transition des éléments graphiques et typographiques sont au coeur du motion design. La production motion design peut déboucher sur la réalisation de film interactif, d’animations web CSS avancées, de courts-métrages vidéo, de présentation d’entreprise, de séquences animées en 3 dimensions. Le motion design n’est pas une fonction exclusive au web et au digital, néanmoins c’est une fonction très représentée dans les métiers du design et de la production dans l’industrie digitale. De nombreux profils de motion designers excercent en tant que freelance indépendants, cela nécessite toutefois de disposer d’un portfolio de travaux en motion design pour communiquer et valoriser le savoir-faire.</li>
        <li>Le motion designer représente toujours une spécialisation pour un concepteur graphique issu des métiers du design (directeur artistique digital, graphiste web, expert en développement front avec de bonnes aptitudes graphiques…). Le profil du motion designer peut également être celui d’un concepteur vidéaste, scénariste, spécialiste du montage et des effets spéciaux. Il existe un lien direct entre l’infographie 3D et le design d’animation. En effet, les séquences de travaux en 3 dimensions font souvent l’objet de séquence interactive animée dans le cadre de communication visuelle avancée, de publicité ou de présentation de produit à forte valeur ajoutée. On peut donc tout à fait parler de motion designer 3D.</li>
        <li>Le motion design a beaucoup évolué dans l’industrie digitale. Les motion designer des années 2000 ont connu l’essor de flash. Aujourd’hui, le motion passe par les animations CSS et javascript, mais également le montage vidéo et les séquences d’animation interactives. Le motion designer assure avant tout la production des séquences numériques animées (vidéo, animation, jeux). Suppose des capacités de scénarisation mais également un bagage technique solide pour la mise en oeuvre, sans compter les qualités de designer. Un métier très complet.</li>
        </ul>
        <h2>Fiche métier motion designer</h2>
        <p>Rôle du motion designer : le motion designer est spécialisé dans le design d’interaction et les séquences animées plus pécisément les vidéos interactives, ainsi que les films interactif, animation.</p>
        <img src="../assets/images/pictures/etienne-2.png" alt="couv">
        <h2>Intervention dans le projet digital</h2>
        Il intervient en phase de conception production (prototypage et finalisation) et de gestion : le motion designer gère une à trois productions en fonction des besoins. La production d’un film interactif exige une disponibilité dédiée. Les Tâches quotidiennes sont :
        <ul>
        <li>scénarisation, conception, production, optimisation, étalonnage… Qualités du motion designer : visualisation, écoute, adaptation, créativité, projection.</li>
        <li>Perspectives d’évolutions : cinéma, publicité, scénariste, production cinématographique.</li>
        <li>Outils de prédilection : logiciel de montage et de création vidéo, fx, css3 html5, javascript (anciennement flash, director). Tarif du motion designer, facturation jour/homme : entre 350 et 700 € HT selon profil et expérience. La Rémunération motion designer se situe à peu près à 20KE à 40KE annuel selon profil et expérience. Profil et formation du motion designer : école arts graphiques, autodidacte.</li>
        </ul>

