                    Le motion designer représente toujours une spécialisation pour un concepteur graphique issu des métiers du design ( <span class="mediumgrey">directeur artistique digital, graphiste web, expert en développement front avec de bonnes aptitudes graphiques</span> ). Le profil du motion designer peut également être celui d’un concepteur vidéaste, scénariste, spécialiste du montage et des effets spéciaux. Il existe un lien direct entre l’infographie 3D et le design d’animation. <br> En effet, les séquences de travaux en 3 dimensions font souvent l’objet de séquence interactive animée dans le cadre de communication visuelle avancée, de publicité ou de présentation de produit à forte valeur ajoutée. On peut donc tout à fait parler de motion designer 3D.<br><br>
                
                    Le motion design a beaucoup évolué dans l’industrie digitale. Les motion designer des années 2000 ont connu l’essor de flash. Aujourd’hui, le motion passe par les animations CSS et javascript, mais également le montage vidéo et les séquences d’animation interactives. Le motion designer assure avant tout la production des séquences numériques animées ( <span class="mediumgrey">vidéo, animation, jeux</span> ). Suppose des capacités de scénarisation mais également un bagage technique solide pour la mise en oeuvre, sans compter les qualités de designer. Un métier très complet.
