            <p>
                L’Univers du web regorge de subtilités. A l’instar d’une toile d’araignée numérique et bien que différents, des connecteurs logiques se font entre les différentes visions de ces professions. Cependant, il est intéressant de noter que chez certains professionnels du web les missions peuvent être divergentes selon les points de vue. <br><br>
                
                Par exemple, le poste de graphiste et de designer web peut être amalgamé chez certains professionnels. D’une entreprise à une autre les missions d’un designer web peuvent se révéler être différentes selon les structures d’accueils.  Bien entendu, ces deux métiers ont une base procédurale commune et font partie d’un même univers.
            </p>

            <img src="../assets/images/pictures/photo_12.jpg" alt="Réunion entre professionnels du web">

            <p>
                Toutefois, les missions qui en résultent peuvent avoir des finalités différentes et in fine provoquer la confusion dans l’esprit collectif des néophytes.  De ce fait, ce sera l’objet de mon article : analyser les facteurs économique et culturels qui influencent la vison des métiers du web. 
            </p>
        </div>

        <div>
            <h2> La trinité Designer Web / Graphiste / Développeur Web </h2>

            <p>    
                Commençons par ce qui fait le cœur de notre formation ; devenir « web designer ». Un web designer est un concepteur de site internet. Plus précisément, il a pour rôle de concevoir l’architecture web en prenant en compte pléthore de variables. En effet, il doit réaliser ce travail de manière à optimiser l’ergonomie de son site afin de favoriser in fine l’appréciation de l’utilisateur. En résumé il crée les différentes étapes de la conception en amont comprenant les éléments d’un site web. Il commence par le zoning qui constitue un premier jet de ce que sera la futur page web. Ensuite il procède à la réalisation du wireframe, puis de la maquette jusqu’au prototype. De ce fait il a pour rôle de faire la passerelle entre le graphiste et le développeur web. <br> <br>
                
                Le graphiste conçoit les contenus visuels à l’aide d’outils de conception comme la suite adobe Photoshop et Illustrator. Il produit le contenu visuel aux designer web que celui–ci positionnera sur la maquette. <br><br>
                
                Le développeur web quant à lui, intègre le contenu écrit et visuels grâce aux langages front-end HTML5, CSS et JavaScript mais également du langage back-end visant tester un prototype de la page web. Le développeur intervient en fin de procédure pour réaliser une tache d’intégration pure par sa pratique intensive du code et finalise le projet. Ces trois corps de métiers sont donc complémentaires, mais beaucoup d’entreprise font l’erreur de les mettre dans le même panier.
            </p>
        </div>

        <div>
            <h2> La vision business des différents métiers </h2>

            <p>   
                Selon le contexte professionnel, la fiche de poste ne comportera pas les mêmes missions. De ce fait, une entreprise mal informée sur le sujet aura tendance à embaucher un seul et même individu pour les 3 fonctions mentionnées dans le paragraphe précédent. On peut en déduire logiquement que la perception des métiers du web est intimement lié à la culture de l’entreprise et de son domaine d’activité stratégique. <br><br> 
                En effet, une entreprise n’ayant pas le web comme cœur de métier aura logiquement tendance à mal évalué les besoins de l’entreprise sur ce genre de problématique. En revanche une agence web aura a priori les armes nécessaires à l’évaluation de ses besoins même si dans l’absolue elle peut faire des erreurs de recrutement. Il faut inclure également le circuit de recrutement si celui-ci est simple ou complexe. Au sein de ce processus de recrutement on peut prendre en compte si l’entreprise fait appel à un intermédiaire (chasseur de têtes, agence qualifiée en ressource humaine) ou à contrario organise sa propre campagne de recrutement et réaliser la fiche de poste.
            </p>

            <img src="../assets/images/pictures/photo_9.jpg" alt="Personne qui rédige sur une feuille">

            <p>
                Concrètement, il est primordial de savoir qui va rédiger la fiche de poste. <br><br>

                Dans le cas d’une multinationale ayant un Framework managérial structuré et hiérarchisez, la tache incombera au département des ressources humaines.Pour une petite structure issue d’un environnement managériale, ce sera le dirigeant de l’entreprise ou un responsable tiers qui éditera la fiche de poste. Le Leadership du dirigeant a également un impact sur le rôle et l’attribution des missions. Un leadership autoritaire favorisera une homogénéité des missions tandis qu’un leadership participatif impliquera une hétérogénéité des tâches à accomplir. <br><br> 
                
                De plus, on peut impliquer des facteurs économiques assujettit à la situation financière et à la taille de l’entreprise. Une startup en plein balbutiement pourra faire l’économie d’un des postes afin de payer des salaires supplémentaires ou se constituer un pool de stagiaires constituant une main d’œuvre bon marché. Une société anonyme du CAC 40 aura les moyens financiers d’embaucher plus de personnel. <br><br>
                
                Ainsi dans une petite structure le designer web endossera plusieurs casquettes impliquant des missions de graphistes et des missions de web développeur.
            </p>
