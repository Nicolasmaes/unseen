            <div class="big_title">
                <h1> Le métier de Directeur Commercial </h1>
                <p> Article réalisé et rédigé par <a href="https://github.com/paule29" target="_blank"><span class="lightgrey">Paule</span>.</a></p>
            </div>

            <p>
                Un site internet est certainement la plus belle vitrine qu’une entreprise ait besoin. Pour la valoriser en terme de communication web, il faut penser à son intérêt et son intelligence commerciale et marketing. C’est un sujet parfois négligé lors de la création ou la refonte d’un site internet. <br><br>
                Plus qu’un simple outil, il doit être un véritable ambassadeur et facilitateur de business s’il est correctement pensé et aménagé. Il devient même un véritable outil d’aide à la vente. Afin d’avoir des retombées économiques, pensons webmarketing, à l’analyse des statistiques du site enfin en faire un véritable outil commercial.
            </p>

            <img src="../assets/images/pictures/photo_10.jpg" alt="Un commercial qui écrit sur un paperboard">

            <p>    
                Le Directeur commercial / e-commerce définit la stratégie marketing et commerciale associée au commerce électronique en cohérence avec la stratégie globale et multi-canal de l’entreprise. Il encadre les équipes internes ou coordonne les prestataires externes et pilote les projets afin d’atteindre les objectifs commerciaux fixés par la direction. Ses missions sont diverses et variées. Son objectif étant d’accroitre le trafic et la notoriété du site, fidéliser les internautes.
            </p>  
        </div> 

        <div>
            <h2 class="red"> Activités principales </h2>
            <p>
                Manage ses équipes, est l’intermédiaire entre celles-ci et les autres services de l’entreprise. Assure la création et élabore la stratégie e-commerce de l’entreprise en mettant en place une stratégie marketing online ( <span class="lightgrey">mise en ligne et référencement de toutes les opérations commerciales</span> ). <br><br>

                S’occupe du développement commercial sur le web, il analyse le marché afin de trouver la solution adaptée. <br><br>

                Propose un projet de stratégie e-commerce en tenant compte des autres canaux de vente. Argumente ses orientations innovantes en fonction des évolutions du marché et des changements technologiques. <br><br>

                Garanti la cohérence des actions du e-commerce avec la stratégie marketing de l’entreprise. Assure une veille concurrentielle commerciale, mais aussi technologique sur un plan national et international ( <span class="lightgrey">participe à des conférences sur l’expérience multicanale</span> ). <br><br><br><br>



                Défini les projets ou les études à lancer nécessaires à l’évolution et au développement du e-commerce, propose un budget prévisionnel. <br><br>

                Assure l’interface entre le comité de direction et ses équipes, relaie les objectifs spécifiques pour les activités web. <br><br>

                Met en place la politique de sécurité en matière d’outils et d’échanges de données, contribue aux projets de l’entreprise. <br><br>

                Développe des partenariats avec des fournisseurs d’accès, des sites communautaires ( <span class="lightgrey">Facebook</span> ). <br><br>

                Analyse les indicateurs de gestion commerciale et arbitre le pilotage de l’activité afin de garantir l’atteinte des objectifs fixés. 
            </p>

            <img src="../assets/images/pictures/photo_11.jpg" alt="Post-it collé sur un paperboard">

            <p>
                Met en place les moyens humains, financiers et techniques nécessaires à la mise en œuvre de la stratégie e-commerce. Elabore des plans d’actions spécifiques au regard des indicateurs comme le chiffre d’affaires, les quantités vendues, les marges. Enfin, il fera évoluer le site afin de développer les ventes. <br><br>

                Représente l’enseigne dans des instances internes ou externes, son champ d’action peut être défini à une région, un pays ou une zone géographique. <br><br>

                Organise les campagnes de communication liées au e-commerce, gestion du centre de profit, analyse les tableaux. <br><br>

                Gère le compte d’exploitation e-commerce, suit le budget défini avec la direction générale, élabore des animations. Il coordonne les activités et les projets. <br><br>

                Gère les ressources humaines de sa propre direction, recrute, intègre les nouveaux arrivants. Motive le personnel en leur fixant des objectifs, contribue à la formation et au développement des compétences de ses équipes. <br><br>

                Réalise, organise, planifie l’activité et ajuste les prévisions en collaboration avec ses responsables. Assure un suivi régulier de l’avancée des projets avec ses équipes, répond aux sollicitations de ses collaborateurs. Dans les entreprises qui ont des achats spécifiques pour le e-commerce. Le directeur e-commerce aura en charge les flux logistiques ou la gestion du sous-traitant.
            </p>
        </div>

        <div>
            <h2 class="red"> Conclusion </h2>

            <p>
                En bref, les activités du directeur commercial ou e-commerce constitue une activité émergente en cours de structuration l’effectif afférent est faible et les activités ne seront que partiels, centré sur le suivi et le développement commercial. <br><br>

                Les activités e-commerce peuvent être rattachées à la direction marketing. La mission du directeur e-commerce est de gérer le site sur tous les fronts. Il lui revient de le faire évoluer en permanence auprès de la demande des consommateurs et doit toujours être en veille pour maitriser le e-commerce et augmenter le chiffre d’affaire grâce aux ventes en ligne ou sur mobile de son entreprise en optimisant le site marchand. <br><br>

                En définissant et pilotant les stratégies web marketing, il assure le suivi et le développement commercial. De ce fait, certaines entreprises sous-traitent une partie des activités liées au e-commerce comme par exemple la partie studio création. Dans ce cas, le directeur e-commerce est amené à suivre et à coordonner le travail des agences de création.
            </p>
