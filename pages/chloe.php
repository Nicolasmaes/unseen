            <div class="article__group">
                <p>
                    Le développeur rédige le code informatique d’un produit / site / programme répondant aux exigences du cahier des charges rédigé par le chef de projet en fonction des exigences du client et des besoins fonctionnels.
                    Il analyse les besoins, choisit la solution technique la mieux adaptée et développe les fonctionnalités du site web ou de l’application web de sa phase d’étude à son intégration.
                </p>

                <img src="../assets/images/pictures/chloe_picture_one.jpg" alt="couv">

                <p>
                    Ses autres missions consistent a :<br><br>
            
                    Corriger les problèmes des projets développés sans interrompre le fonctionnement / assurer le support technique. <br>
                    Former le client à la réception du site. <br>
                    Rédiger notices et guides pour les utilisateurs.
                </p>
            </div>

            <div class="article__group">
                <h2 class="purple">Les catégories de développeur web</h2>
             
                <p>
                    <span class="bold">Developpeur back-end</span> <br><br>
                 
                    Il s’occupe de la programmation du côté du serveur : il crée le code et les programmes qui alimentent le serveur du site web, les bases de données et toutes les applications que contient le site internet. <br><br>

                    Il utilise les différents langages côté serveur : <br>
                    PHP / Python / Java / Ruby / SQL ( <span class="mediumgrey">pour gérer les données dans les bases de données des sites</span> ). <br><br><br>
               

                    <span class="bold">Développeur front-end</span> <br><br>
                    
                    Il se charge de la conception du site web côté client et écrit le code nécessaire pour l’implémenter sur le web. <br><br>
                    
                    <span class="double_titre ">
                        <span>
                            Il utilise les différents langages côté client : <br>
                            HTML / CSS <br>
                            JavaScript
                        </span>   

                        <span>
                            Il peut également réaliser : <br>
                            Le référencement naturel ( <span class="mediumgrey">SEO</span> ) <br>
                            Les tests d’utilisabilité, d’accessibilité et de performances
                        </span>
                    </span> <br><br>
         
                    <span class="bold">Développeur full stack</span> <br><br>

                    Il réunit les 2 facettes front-end et back-end. <br><br>
                <p>
               
                <img src="../assets/images/pictures/chloe_picture_two.jpg" alt="couv">

                <p>
                    
                    <span class="bold">Développeur web mobile</span> <br><br>

                    Il effectue la réalisation technique et le développement informatique d’applications pour mobiles, smartphones et autres tablettes. Ses responsabilités sont de tenir compte des spécificités de l’affichage des applications sur les différents outils. <br><br>

                    Pour cela, il utilise différents langages selon les systèmes d’exploitation :<br><br>
               
                    C / Objective C / HTML5 ( <span class="mediumgrey">iOS</span> ) <br>
                    C / C# / PHP / Java ( <span class="mediumgrey">Android</span> ) <br><br>

                    <span class="bold">Qualités recherchées</span> <br><br>
                
                    Polyvalence <br>
                    Autonomie <br>
                    Respect des délais <br>
                    Veille technologique <br>
                </p>
            </div>

            <div class="article__group">
                <h2 class="purple">Où exerce t-on ces métiers ?</h2>

                <p>
                    Cette activité s’exerce au sein de sociétés de services, d’entreprises, de collectivités territoriales, d’entreprises publiques, en indépendant etc. <br>
                    Elle varie selon la taille de la structure ( <span class="mediumgrey">polyvalence, spécialisation en gestion du projet, programmation, recettes, etc</span> ) et la responsabilité ( <span class="mediumgrey">développement, projet, etc</span> ). <br><br>
                    
                    Le salaire est également fonction de ces différents paramètres. En moyenne, un développeur web junior peut percevoir une rémunération brute mensuelle de 2100 €. Mais certains atteignent les étoiles tels les développeurs de la Silicon Valley qui peuvent recevoir un salaire de 500 000 $ … cela laisse rêveur ! <br><br>
                
