            <h2 class="red">La question qui fâche</h2>

            <p>
                J’ai pu rencontrer une développeuse junior qui a osé poser la question suivante : <quote>Pourquoi les développeurs juniors ne sont pas ou peu recrutés ?</quote> <br><br><br>
    

                Elle a reçu des dizaines de réponses de gens du métiers expérimentés qui ont donné des pistes de réflexions très intéressantes.<br>
                Les conseils qui lui ont été donnés sont les suivants : <br><br>

                Pratiquer le plus possible afin de pouvoir montrer des projets que nous avons réalisé et de convaincre de sa passion pour le code.
                Durant certains recrutements, il est question de savoir si le candidat sait résoudre un problème seul, et si oui en combien de temps. Il apparaît clair qu’un développeur ne doit pas attendre qu’on le prenne par la main.
                Développer son réseau est très important pour trouver des pistes et savoir se faire remarquer par les bonnes personnes.
                La maîtrise de Git semble être un vrai point fort chez un junior.
            </p>
        </div>

        <div>
            <h2 class="red">Conclusion</h2>

            <p>Pour contraster cet article, nous avons toujours peur de perdre du temps à apprendre quelque chose qui ne nous servira pas, mais les connaissances sont souvent transposables d’une technologie à une autre. S’intéresser à un langage un peu obscur ne sera finalement pas vain.<br>
            Évidemment il faut aussi se sentir à l’aise dans l’écriture informatique, les développeurs ont souvent une affinité avec un langage particulier.</p>
