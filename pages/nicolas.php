        <div>
            <h2 class="red">Langages de balisages</h2>

            <p>
            <span class="bold">HTML ( HyperText Markup Language )</span> <br><br>
            Le langage de balisage le plus couramment utilisé sur le World Wide Web est le HTML. En HTML, on utilise des balises prédéfinies afin de préciser à l’intérieur d’un fichier texte des éléments tels que les titres, les paragraphes, les acronymes, les citations…
            </p>

            <p>
            <span class="bold">CSS ( Cascading Style Sheets )</span> <br><br>
            Signifie en français « feuillets de style en cascade ».
            Celui-ci décrit la présentation des documents HTML.
            </p>
        </div>

        <div>
            <h2 class="red">Langages de programmation</h2>

            <p>
                Un langage de programmation est une notation conventionnelle destinée à formuler des algorithmes et produire des programmes informatiques qui les appliquent. D’une manière similaire à une langue naturelle, un langage de programmation est composé d’un alphabet, d’un vocabulaire, de règles de grammaire, de significations, mais aussi d’un environnement de traduction censé rendre sa syntaxe compréhensible par la machine. Ils sont utilisés aussi bien dans le développement front-end que dans le développement back-end.
            </p>
        </div>

        <div>
            <h2 class="red">
                Framework ( cadre de travail ) & Library ( bibliothèque ) <br> 
                Comment distinguer les deux ?
            </h2>

            <p>
                <span class="bold">Framework</span> <br><br>
                Un framework désigne un ensemble cohérent de composants logiciels structurels, qui sert à créer les fondations ainsi que les grandes lignes de tout ou d’une partie d’un logiciel. Un framework se distingue d’une simple bibliothèque logicielle principalement par :

                Son caractère générique, faiblement spécialisé, contrairement à certaines bibliothèques; Un framework peut à ce titre être constitué de plusieurs bibliothèques, chacune spécialisée dans un domaine.
                Un framework peut néanmoins être spécialisé, sur un langage particulier, une plateforme spécifique, un domaine particulier : communication de données, data mapping…
                Le cadre de travail qu’il impose de par sa construction même, guidant l’architecture logicielle voire conduisant le développeur à respecter certains patrons de conception ; les bibliothèques le constituant sont alors organisées selon le même principe.
                Les frameworks sont donc conçus et utilisés pour modeler l’architecture des logiciels applicatifs, des applications web, des middlewares et des composants logiciels. Les frameworks sont acquis par les informaticiens, puis incorporés dans des logiciels applicatifs mis sur le marché, ils sont par conséquent rarement achetés et installés séparément par un utilisateur final.
            </p>

            <p>
                <span class="bold">Bibliothèque logicielle</span> <br><br>

                C’est une collection de routines, qui peuvent être déjà compilées et prêtes à être utilisées par des programmes. Les bibliothèques sont enregistrées dans des fichiers semblables, voire identiques aux fichiers de programmes, sous la forme d’une collection de fichiers de code objet rassemblés accompagnée d’un index permettant de retrouver facilement chaque routine.
            </p>

            <p>
                En deux mots,
                <quote>Le développeur utilise une librairie en appelant le code de cette dernière, tandis que le rôle du framework est d’exécuter le code du développeur.</quote>
                <quote>Voyez la bibliothèque comme une boîte à outils et le framework comme un laboratoire.</quote></p>
        </div>

        <div>
            <h2 class="red">CMS ( content management system )</h2>

            <p>
                <span class=bold>( Typo3, Joomla, Drupal, WordPress, Contao, Neos CMS, Dreamwaver, Craft, Grav, Kirby )</span> <br><br>
                
                CMS signifie en français « système de gestion de contenu ». Il s’agit d’un programme informatique utilisant une base de données et permettant de gérer de A à Z l’apparence et le contenu d’un site web. Il permet, à des individus ou à des groupes hiérarchisés, de mettre à jour le contenu d’un site web à partir d’un panneau d’administration. Aujourd’hui, les CMS sont des plates-formes de déploiement de sites web très populaires, permettant de créer des sites web complexes à moindre coût. Certains CMS incluent également la gestion de version.
            </p>
        </div>

        <div>
            <h2 class="red">Base de données</h2>

            <p>
                Une base de données est un ensemble structuré et organisé permettant le stockage de grandes quantités d’informations afin d’en faciliter l’exploitation (ajout, mise à jour, recherche de données). Ces informations sont en rapport avec une activité donnée et peuvent être utilisées par des programmes ou des utilisateurs communs, d’où la nécessité de leur mise en commun. <br><br>

                Elles sont utilisées dans un grand nombre d’entreprises pour stocker, organiser et analyser les données. <br><br>

                Elles se chargent elles-mêmes de créer, de mettre à jour ou de supprimer des données. Elles effectuent également des recherches parmi les données qu’elles contiennent sur demande de l’utilisateur, et de lancer des applications à partir des données. <br><br>

                L’API standard pour les bases de données relationnelles est le Structured Query Language (SQL). Les bases de données relationnelles sont facilement extensibles, et de nouvelles catégories de données peuvent être ajoutées après la création de la database originale sans avoir besoin de modifier toutes les applications existantes.
            </p>
        </div>

        <div>
            <h2 class="red">Serveurs</h2>

            <p> 
                <span class="bold">( Apache, Laragon, Laravel ) </span> <br><br>

                Un serveur web est soit un logiciel de service de ressources web ( <span class="lightgrey">serveur HTTP</span> ), soit un serveur informatique ( <span class="lightgrey">ordinateur</span> ) qui répond à des requêtes du WWW sur un réseau public ( <span class="lightgrey">Internet</span> ) ou privé ( <span class="lightgrey">Intranet</span> ) en utilisant principalement le protocole HTTP. Un serveur informatique peut être utilisé à la fois pour servir des ressources du Web et pour faire fonctionner en parallèle d’autres services liés comme l’envoi d’e-mails, l’émission de flux streaming, le stockage de données via des bases de données, le transfert de fichiers par FTP, etc.
            </p>
        </div>

        <div>
            <h2 class="red">Logiciels de création graphique</h2>

            <p>
                <span class="bold">( Photoshop, Illustrator, Figma, suite Adobe )</span> <br><br>
                
                Ces logiciels très complets permettent la retouche photo, la création de logo et de design pour habiller des sites web.
            </p>
        </div>

